package main

func minDepth(root *TreeNode) int {
	if root == nil {
		return 0
	}
	return min(minDepth(root.Left), minDepth(root.Right)) + 1

}
func min(a, b int) int {
	if a > b {
		return b
	} else {
		return a
	}
}

