/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
package tree

type TreeNode struct {
	Val   int
	Left  *TreeNode
	Right *TreeNode
}

//ä¸­å·¦å³
func preorderTraversal(root *TreeNode) []int {
	if root == nil {
		return make([]int, 0, 0)
	}
	return append(append([]int{root.Val}, preorderTraversal(root.Left)...), preorderTraversal(root.Right)...)
}

